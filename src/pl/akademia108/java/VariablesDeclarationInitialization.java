package pl.akademia108.java;

public class VariablesDeclarationInitialization {
    public void variablesDeclarationInitializationExample() {

        /* Deklaracja zmiennej */
        int age;

        /* Inicjalizacja zmiennej */
        age = 21;

        System.out.println(age);

        /* Przypisanie nowej wartości do zmiennej */
        age = 30;

        System.out.println(age);

        /* Deklaracja z inicjalizacją */
        double price = 12.99;

        System.out.println(price);

        /* Przypisanie  nowej wartości do deklaracji z inicjalizacją */
        price = 15.99;

        System.out.println(price);



    }

}
